package com.kai.recruit.model;

public class FriendUrlsModel {
    private int urlId;
    private String url;
    private String name;

    public void setUrlId(int urlId) {
        this.urlId = urlId;
    }

    public int getUrlId() {
        return this.urlId;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUrl() {
        return this.url;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

}
